    // Codigo para la ejecucion del boton siguiente para validar el usuario y llevar a sus respectiva pantalla
    public void validarUsuario() {
        String usuario = txtusuario.getText();
        String contra = String.valueOf(passcontra.getPassword());

        UsuariosModel usuarioverificado = UsuriosController.verificarUsuario(usuario, contra);

        if (usuarioverificado != null) {
            JOptionPane.showMessageDialog(this, "¬°Bienvenido " + usuarioverificado.getRolUsuario()+ "!");

            // se obtiene el rol del objeto del usuario verificado
            String rol = usuarioverificado.getRolUsuario();

            if ("Vendedor".equals(rol)) {
                // Si el rol es Vendedor, se abrira la pantalla del vendedor
                FrmMenuVendedor pantallaVendedor = new FrmMenuVendedor();
                pantallaVendedor.setVisible(true);
                this.dispose(); // Cierra la ventana de login
            } else if ("Pastelero".equals(rol)) {
                // Si el rol es Pastelero, se abrira la pantalla del pastelero
                FrmMenuPastelero pantallaPastelero = new FrmMenuPastelero();
                pantallaPastelero.setVisible(true);
                this.dispose(); // Cierra la ventana de login
            } else {
                // Si no hay un rol o usario con esa asigacion
                JOptionPane.showMessageDialog(this, "Rol de usuario no reconocido.");
            }

        } else {
            // Dara un mensaje de error
            JOptionPane.showMessageDialog(this, "Usuario o contrase√±a incorrectos.", "Error de Login", JOptionPane.ERROR_MESSAGE);
            // Limpia los cuadros de texto, por si muestra el mensaje de error
            txtusuario.setText("");
            passcontra.setText("");
        }
    }


