    public void guardarCliente() {
        try {
            // 1. Obtener los datos del formulario y limpiar espacios en blanco
            String nombre = txtnombre.getText().trim();
            String apellidoPaterno = txtapellidopa.getText().trim();
            String apellidoMaterno = txtapellidoma.getText().trim();
            String direccion = txtcolonia.getText().trim();
            String genero = (String) combogenero.getSelectedItem();
            String telefono = txttelefono.getText().trim();
            String edadStr = txtedad.getText().trim();
            String anio = txtanio.getText().trim();
            String mes = txtmes.getText().trim();
            String dia = txtdia.getText().trim();

            // Reemplaza esto con el ID real del usuario que ha iniciado sesión
            int idUsuarioActual = 1;

            // 2. Validar que los campos obligatorios no estén vacíos.
            if (nombre.isEmpty() || apellidoPaterno.isEmpty() || telefono.isEmpty() || edadStr.isEmpty() || anio.isEmpty() || mes.isEmpty() || dia.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.", "Error de Validación", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 3. Validar y convertir la edad.
            int edad;
            try {
                edad = Integer.parseInt(edadStr);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "La edad debe ser un número entero válido.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 4. Validar el formato del teléfono (solo dígitos).
            if (!telefono.matches("\\d+")) {
                JOptionPane.showMessageDialog(this, "El teléfono solo debe contener números.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 5. Validar y construir la fecha de nacimiento.
            String fechaNacimiento;
            try {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                sdf.setLenient(false); // Esta línea hace que la validación sea estricta (ej. 30 de febrero es inválido)
                fechaNacimiento = anio + "-" + mes + "-" + dia;
                sdf.parse(fechaNacimiento);
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(this, "La fecha de nacimiento no es válida. Por favor, verifique el día, mes y año.", "Error de Fecha", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 6. Crear un objeto del modelo con los datos validados.
            ClientesModel cliente = new ClientesModel(
                    nombre,
                    apellidoPaterno,
                    apellidoMaterno,
                    direccion,
                    genero,
                    fechaNacimiento,
                    telefono,
                    edad,
                    idUsuarioActual
            );

            // 7. Llamar al controlador para guardar el cliente.
            boolean guardadoExitoso = ClientesController.guardarCliente(cliente);

            if (guardadoExitoso) {
                JOptionPane.showMessageDialog(this, "Cliente guardado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo guardar el cliente. Verifique los datos o intente nuevamente.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Ocurrió un error inesperado al guardar el cliente.", "Error General", JOptionPane.ERROR_MESSAGE);
        }
    }

